"""Coaching Flow Agent

Merges outputs from resume_coach (coaching_session) and interview_map into a
single, phase-gated coaching session plan the candidate can work through
sequentially.

Phase structure
---------------
  Phase 1 — Internship Recall      (coaching_session.internship_drills)
  Phase 2 — Stock Pitch Prep       (coaching_session.pitch_scaffold)
  Phase 3 — Interview Round Sim    (interview_map.rounds)
  Phase 4 — Mock Plan Execution    (interview_map.mock_plan)

Each phase carries:
  phase_id, phase_name, status (pending/active/done),
  source, items, llm_intro, llm_tips

The agent also emits:
  overall_progress  — {total_phases, completed, pct}
  next_action       — human-readable string telling the candidate what to do now
  coaching_tips     — forwarded from interview_map
  weekly_plan       — 7-day action calendar generated by LLM

Usage (graph mode):
  coaching_flow_agent(state) -> {"coaching_flow": {...}}

Usage (standalone / REPL):
  from app.agents.coaching_flow import advance_phase, get_summary
  new_state = advance_phase(state, phase_id=1)
  print(get_summary(state))
"""
