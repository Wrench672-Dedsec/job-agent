# ---------------------------------------------------------------------------
# Core builder
# ---------------------------------------------------------------------------

def _build_coaching_flow(
    jd_profile: dict,
    diagnosis: dict,
    coaching_session: dict,
    interview_map: dict,
) -> dict:
    job_type = jd_profile.get("job_type", "equity research")
    sector = jd_profile.get("sector", "generalist")

    phases = []
    for ph_def in _PHASE_DEFS:
        items = _get_phase_items(ph_def, coaching_session, interview_map)
        intro = _llm_phase_intro(ph_def, job_type, sector)
        phases.append({
            "phase_id": ph_def["phase_id"],
            "phase_name": ph_def["phase_name"],
            "status": "pending",
            "source": ph_def["source"],
            "description": ph_def["description"],
            "llm_intro": intro,
            "items": items,
            "item_count": len(items),
        })

    completed = sum(1 for p in phases if p["status"] == "done")
    total = len(phases)
    pct = round(completed / total * 100) if total else 0

    weekly_plan = _llm_weekly_plan(jd_profile, diagnosis, coaching_session, interview_map)

    coaching_tips = interview_map.get("coaching_tips") or [
        "\u6bcf\u9898\u4e0d\u8d85\u8fc7 90 \u79d2",
        "\u4e0d\u8981\u80cc\u7a3f",
        "wind-down \u65f6\u4e3b\u52a8\u95ee\u4e00\u4e2a\u806a\u660e\u7684\u95ee\u9898",
    ]

    return {
        "phases": phases,
        "overall_progress": {
            "total_phases": total,
            "completed": completed,
            "pct": pct,
        },
        "next_action": _NEXT_ACTION_TEMPLATES.get(completed, _NEXT_ACTION_TEMPLATES[0]),
        "coaching_tips": coaching_tips,
        "weekly_plan": weekly_plan,
        "meta": {
            "job_type": job_type,
            "sector": sector,
            "total_items": sum(p["item_count"] for p in phases),
        },
    }


# ---------------------------------------------------------------------------
# Public agent (graph mode)
# ---------------------------------------------------------------------------

def coaching_flow_agent(state: InvestmentJobState) -> Dict[str, Any]:
    jd_profile = state.get("jd_profile") or {}
    diagnosis = state.get("diagnosis") or {}
    coaching_session = state.get("coaching_session") or {}
    interview_map = state.get("interview_map") or {}

    coaching_flow = _build_coaching_flow(
        jd_profile=jd_profile,
        diagnosis=diagnosis,
        coaching_session=coaching_session,
        interview_map=interview_map,
    )

    total = coaching_flow["meta"]["total_items"]
    logger.info(
        "coaching_flow_agent: %d phases built, %d total items.",
        len(coaching_flow["phases"]),
        total,
    )
    return {"coaching_flow": coaching_flow}


# ---------------------------------------------------------------------------
# Standalone helpers (call directly, not via graph)
# ---------------------------------------------------------------------------

def advance_phase(state: InvestmentJobState, phase_id: int) -> InvestmentJobState:
    """Mark a phase as done and update progress. Returns updated state."""
    cf = dict(state.get("coaching_flow") or {})
    phases = [dict(p) for p in cf.get("phases", [])]
    for p in phases:
        if p["phase_id"] == phase_id:
            p["status"] = "done"
    completed = sum(1 for p in phases if p["status"] == "done")
    total = len(phases)
    cf["phases"] = phases
    cf["overall_progress"] = {
        "total_phases": total,
        "completed": completed,
        "pct": round(completed / total * 100) if total else 0,
    }
    cf["next_action"] = _NEXT_ACTION_TEMPLATES.get(completed, _NEXT_ACTION_TEMPLATES[4])
    return {**state, "coaching_flow": cf}


def get_summary(state: InvestmentJobState) -> str:
    """Return a human-readable coaching flow summary."""
    cf = state.get("coaching_flow") or {}
    if not cf:
        return "coaching_flow not yet generated. Run coaching_flow_agent first."
    prog = cf.get("overall_progress", {})
    lines = [
        f"=== Coaching Flow Summary ===",
        f"Progress: {prog.get('completed', 0)}/{prog.get('total_phases', 4)} phases "
        f"({prog.get('pct', 0)}%)",
        f"Next: {cf.get('next_action', '')}",
        "",
        "Phases:",
    ]
    for p in cf.get("phases", []):
        status_icon = "\u2705" if p["status"] == "done" else "\u23f3"
        lines.append(
            f"  {status_icon} Phase {p['phase_id']}: {p['phase_name']} "
            f"({p['item_count']} items) [{p['status']}]"
        )
    lines.append("")
    lines.append("Weekly Plan Milestones:")
    for ms in (cf.get("weekly_plan") or {}).get("key_milestones", []):
        lines.append(f"  • {ms}")
    return "\n".join(lines)
