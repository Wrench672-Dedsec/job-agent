def _get_phase_items(phase_def: dict, coaching_session: dict, interview_map: dict) -> List[Any]:
    src = phase_def["source"]
    if src == "coaching_session.internship_drills":
        return coaching_session.get("internship_drills") or []
    if src == "coaching_session.pitch_scaffold":
        scaffold = coaching_session.get("pitch_scaffold")
        return [scaffold] if scaffold else []
    if src == "interview_map.rounds":
        return interview_map.get("rounds") or []
    if src == "interview_map.mock_plan":
        mp = interview_map.get("mock_plan")
        return [mp] if mp else []
    return []


